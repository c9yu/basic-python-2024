# 변수(Variable) - 변하는 수. 파이썬에서는 들어가는 종류나 크기에 제약이 없음
var01 = 10
print(var01)

var01 = 100000000000000000000000000000
print(var01)

# 줄이 너무 길어 화면 밖으로 나가는 경우 경우 '보기' 탭에서 자동 줄바꿈을 사용
var01 = 9999999999999999999999999999999999999999999999999999999999 
print(var01)

var01 = False
print(var01)

var01 = 'Life is short, You need Python'
print(var01)

